#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    results = []
    helper(sentence, 0, len(sentence), Dict, [], results)
    return results

def helper(sentence, start, end, dict, temp, results):
    if start >= end:
        results.append(temp[::])
        return 
    
    for word in dict.keys():
        if sentence[start:start + len(word)] == word:
            temp.append(word)
            helper(sentence, start + len(word), end, dict, temp, results)
            temp.pop()
    




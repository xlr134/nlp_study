#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
max_len = max([len(i) for i in Dict.keys()])

def all_cut(sentence, Dict):
    result = []
    if not sentence:
        return [[]]
    for leng in range(1, min(len(sentence) + 1, max_len + 1)):
        pretx = sentence[:leng]
        if pretx in Dict:
            all_cuts = all_cut(sentence[leng:], Dict)
            for senten in all_cuts:
                result.append([pretx] + senten)
    return result


result = all_cut(sentence, Dict)
for i in result:
    print(i)


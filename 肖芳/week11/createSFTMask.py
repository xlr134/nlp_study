import numpy as np
import torch

def createSFTMask2(s_len, index):
  arr = np.ones((s_len, s_len))

  arr[:index, index:] = 0.0
  i = index + 1
  for row in arr[index:]:
    row[i:] = 0.0
    i += 1
  return arr



#词典；每个词后方存储的是其词频，词频仅为示例，不会用到，也可自行修改
Dict = {"经常":0.1,
        "经":0.05,
        "有":0.1,
        "常":0.001,
        "有意见":0.1,
        "歧":0.001,
        "意见":0.2,
        "分歧":0.2,
        "见":0.05,
        "意":0.05,
        "见分歧":0.05,
        "分":0.1}

#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    results = []

    def cut_helper(start, temp_result):
        if start == len(sentence):
            # 当递归到达句子末尾时，将当前切分结果添加到最终结果中
            results.append(list(temp_result))
            return

        for i in range(start, len(sentence) + 1):
            word = sentence[start:i]
            if word in Dict:
                # 如果当前词在词典中，则将其添加到临时结果中，并继续递归处理剩余部分
                temp_result.append(word)
                cut_helper(i, temp_result)
                # 回溯：移除刚刚添加的词，以便尝试其他切分方式
                temp_result.pop()

    # 从句子开头开始递归切分
    cut_helper(0, [])

    return results

# 调用全切分函数并打印结果
target = all_cut(sentence, Dict)
for cut in target:
    print(cut)


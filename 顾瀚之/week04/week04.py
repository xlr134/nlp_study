#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    if len(sentence) == 0:
        return []
    res = []
    for i in range(1, len(sentence)+1):
        prefix = sentence[:i]
        suffix = sentence[i:]
        if prefix in Dict:
            rest = all_cut(suffix, Dict)
            if len(rest) == 0:
                res.append([prefix])
            for a in rest:
                res.append([prefix] + a)

    return res

